const express = require('express');
const cors = require('cors');
const helmet = require('helmet');
const compression = require('compression');
const morgan = require('morgan');
const path = require('path');
require('dotenv').config();

const database = require('./config/database');
const atecoRoutes = require('./routes/ateco');
const { router: impreseRoutes } = require('./routes/imprese');

const app = express();
const PORT = process.env.PORT || 3001;

// Middleware
app.use(helmet({
  contentSecurityPolicy: false, // Disable for development
}));
app.use(compression());
app.use(morgan('combined'));
app.use(cors({
  origin: process.env.CLIENT_URL || 'http://localhost:3000',
  credentials: true
}));
app.use(express.json({ limit: '10mb' }));
app.use(express.urlencoded({ extended: true, limit: '10mb' }));

// API Routes
app.use('/api/ateco', atecoRoutes);
app.use('/api/imprese', impreseRoutes);

// Health check endpoint
app.get('/api/health', (req, res) => {
  res.json({ 
    status: 'OK', 
    timestamp: new Date().toISOString(),
    version: '1.0.0'
  });
});

// Serve static files in production
if (process.env.NODE_ENV === 'production') {
  app.use(express.static(path.join(__dirname, '../client/build')));
  
  app.get('*', (req, res) => {
    res.sendFile(path.join(__dirname, '../client/build', 'index.html'));
  });
}

// Error handling middleware
app.use((err, req, res, next) => {
  console.error('Error:', err);
  res.status(500).json({ 
    error: 'Internal Server Error',
    message: process.env.NODE_ENV === 'development' ? err.message : 'Something went wrong'
  });
});

// 404 handler
app.use((req, res) => {
  res.status(404).json({ error: 'Endpoint not found' });
});

// Initialize database and start server
async function startServer() {
  try {
    await database.connect();
    console.log('âœ… Database connected successfully');
    
    // Keep database connection alive
    setInterval(() => {
      if (database.db) {
        database.db.get('SELECT 1', (err) => {
          if (err) {
            console.error('Database connection lost, reconnecting...', err);
            database.connect().catch(console.error);
          }
        });
      }
    }, 30000); // Check every 30 seconds
    
    app.listen(PORT, () => {
      console.log(`ğŸš€ Server running on port ${PORT}`);
      console.log(`ğŸ“Š API available at http://localhost:${PORT}/api`);
      console.log(`ğŸŒ Environment: ${process.env.NODE_ENV || 'development'}`);
    });
  } catch (error) {
    console.error('âŒ Failed to start server:', error);
    process.exit(1);
  }
}

startServer();

module.exports = app;
